let value;

// Operador de atribuição
value = 1;
console.log(value);

// Incremento
value += 2;
console.log(value);

// Decremento
value -= 1;
console.log(value);

// Multiplicação

value *= 9;
console.log(value);

// Divisão

value /= 2;
console.log(value);

// Resto da divisão

value %= 2;
console.log(value);

// Exponencial
value **= 2;
console.log(value);
