let date = new Date("July 3, 2024 14:30:00");
console.log(date);

// Modificar ano.
date.setFullYear(2030);

// Modificar mês(começa com 0).
date.setMonth(6);

// Modificar dia.
date.setDate(10);

// Modificar hora, minuto e segundo.
date.setHours(10);
date.setMinutes(20);
date.setSeconds(22);

console.log(date);
